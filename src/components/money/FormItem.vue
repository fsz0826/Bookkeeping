<template>
  <div>
    <label class="formItem">
      <span class="name">{{fieldName}}</span>
      <input type="text"
             :value="value"
             @input="onValueChanged($event.target.value)"
             :placeholder="placeholder">
    </label>
  </div>
</template>

<script lang="js">
  export default {
    name: "FormItem",
    props:['value','fieldName','placeholder'],
    methods:{
      onValueChanged(value) {
        this.$emit("update:value", value);
      }
    }
  }
</script>

